#Dictionary
''' it is the same concept of keep the data into a single variable with key and value pair'''
school={
     'name':'izzan',
     'school':'abcd school',
     'email':'izzan@gmail.com',
     'address':'Dubai Mall'
     }

#printing the whole dictionary
print(school)

#printing the only value
print(school['email'])

#changing some value
school['address']='Mall of Emirates'

print(school)

#adding some value
school['phone']='852963741'

print(school)

#deleting the value

school.clear()

print(school)





