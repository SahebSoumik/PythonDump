#tuple : it is used to store multiple value in a single variable
# tuple is used to declare like '('   ')'
demotup=('coconut','apple','mango','orange','guava','lichi','nut')

print(demotup)

#to access the single value in a tuple

print(demotup[2])

#to access the multiple value

print(demotup[2:4])

#to change the tuple items

x = ("apple", "banana", "cherry")
y = list(x)
y[1] = "kiwi"
x = tuple(y)

print(x)

