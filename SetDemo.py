#What is set ?
'''
set is used store multiple values in a single variable

how to declare the set ? : '{' '}'

1. set are unchangable
2. we can add the value in a set
3. we can remove the value from the set
'''

demoset={'apple','mango','banana','coconut'}

print(demoset)

#set items insert

demoset.add('orange')

print(demoset)

#delete setitems

demoset.remove('coconut')

print(demoset)

# running loops using the set

for i in demoset:
     print(i)



